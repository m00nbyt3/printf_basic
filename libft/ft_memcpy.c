#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t				i;
	const unsigned char	*str1;
	unsigned char		*str2;

	if (!(src || dst))
		return (0);
	str1 = (const unsigned char *)src;
	str2 = (unsigned char *)dst;
	i = -1;
	while (++i < n)
		str2[i] = str1[i];
	return (dst);
}
